// Deawbacks of Arrays in Java
//
// 1. Arrays are fixed in size. Once we create an array, we cannot change its size.
// 2. Arrays can store only similar types of data. For example, we cannot store an int and a string in the same array.
// 3. Arrays are stored in the contiguous memory locations. It makes it difficult to insert or delete the elements from the array because the whole array needs to be shifted in memory.
// 4. We can access only the elements of an array using a loop. It is not possible to access a particular element using an index.
// 5. Arrays do not provide any inbuilt methods to sort the data.
// 6. Arrays do not provide any inbuilt methods to search the data.
// 7. Arrays do not provide any inbuilt methods to merge the data.
// 8. Arrays do not provide any inbuilt methods to split the data.
// 9. Arrays do not provide any inbuilt methods to compare the data.
// 10. Arrays do not provide any inbuilt methods to reverse the data.
// 11. Arrays do not provide any inbuilt methods to shuffle the data.
// 12. Arrays do not provide any inbuilt methods to print the data.
// 13. Arrays do not provide any inbuilt methods to copy the data.
// 14. Arrays do not provide any inbuilt methods to clone the data.
// 15. Arrays do not provide any inbuilt methods to convert the data.


