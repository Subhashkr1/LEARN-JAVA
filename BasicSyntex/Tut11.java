// Logical Operator in java

public class Tut11 {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = 30;
        int d = 40;
        int e = 50;

        // Logical Operator
        System.out.println(a > b && a < c);
        System.out.println(a > b || a < c);
        System.out.println(!(a > b));
    }
    
}
