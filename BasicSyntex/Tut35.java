// Mutable vs Immutable String in Java

public class Tut35 {
    public static void main(String[] args) {
        String name = "Harry";
        System.out.println(name);
        name = "Rohan";
        System.out.println(name);
        name = "SkillF";
        System.out.println(name);

        // String is immutable
        // String is a final class
        // String is a non-primitive data type
        // String is a reference data type

        
    }
    
}
